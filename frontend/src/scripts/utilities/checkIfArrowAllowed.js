const checkIfArrowAllowed = (itemIndex, connectedToIndex, gridMax) => {
    const gridAllowed = grid[gridMax][itemIndex];
    const element = gridAllowed.find(e => e.sectionNumber == connectedToIndex);
    return element ? element.direction : false;
};

export const showArrow = () => {
    $('.lms-grid-block-arrow').each(function (i) {
        const sectionNumber = $(this).data('section-number');
        const linked = $(this).data('linked-to');
        const gridSize = $(this).closest('.lms-slide').data('section-count');
        const direction = checkIfArrowAllowed(sectionNumber, linked, gridSize);
        if (direction) $(this).addClass(`lms-grid-block-arrow-${direction}`);
    });
};